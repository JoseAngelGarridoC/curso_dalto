# Creando una tupla a partir de una lista
lista = ['carro','camion','moto','bicileta']
tupla = tuple(lista)
print('Lista =>',lista)
print('Tupla =>',tupla)

# Creando una tupla sin paréntesis
tupla_2 = 'amarillo','azul','rojo'
print('Tupla sin paréntesis =>',tupla_2)

# Creando una tupla de un solo dato
tupla_3 = ('equipo',) # Se coloca la coma al final para indicar que es una tupla, sino sería un string
print('Tupla de un solo dato =>',tupla_3)